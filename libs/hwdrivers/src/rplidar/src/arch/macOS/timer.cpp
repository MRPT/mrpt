#include "arch/macOS/arch_macOS.h"

namespace rp::arch
{
_u64 getus()
{
	timeval now;
	gettimeofday(&now, nullptr);
	return now.tv_sec * 1000000 + now.tv_usec;
}

_u32 rp_getms()
{
	timeval now;
	gettimeofday(&now, nullptr);
	return now.tv_sec * 1000L + now.tv_usec / 1000L;
}
}  // namespace rp::arch
